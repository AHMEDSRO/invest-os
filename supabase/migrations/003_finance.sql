-- ===================== الجداول =====================

-- الأشخاص والجهات (كل واحد له صفحته وتاريخه)
create table people (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  note text,
  is_active boolean default true,
  created_at timestamptz default now()
);

-- الديون: عليك (on_me) أو ليك (to_me)
create table debts (
  id uuid primary key default gen_random_uuid(),
  person_id uuid references people(id) on delete cascade,
  direction text not null check (direction in ('on_me','to_me')),
  title text,
  principal numeric not null,
  currency text not null default 'EGP' check (currency in ('EGP','AED')),
  note text,
  status text not null default 'open' check (status in ('open','settled')),
  started_on date,
  created_at timestamptz default now()
);

-- سدادات كل دين بالتاريخ والقيمة وطريقة الدفع
create table debt_payments (
  id uuid primary key default gen_random_uuid(),
  debt_id uuid references debts(id) on delete cascade,
  date date not null,
  amount numeric not null,
  method text,
  note text,
  created_at timestamptz default now()
);

-- حركات الدخل والمصاريف
create table transactions (
  id uuid primary key default gen_random_uuid(),
  date date not null,
  type text not null check (type in ('income','expense')),
  category text,
  description text,
  amount numeric not null,
  currency text not null default 'EGP' check (currency in ('EGP','AED')),
  created_at timestamptz default now()
);

-- ===================== RLS =====================

alter table people enable row level security;
alter table debts enable row level security;
alter table debt_payments enable row level security;
alter table transactions enable row level security;

create policy "authenticated_all" on people
  for all to authenticated using (true) with check (true);
create policy "authenticated_all" on debts
  for all to authenticated using (true) with check (true);
create policy "authenticated_all" on debt_payments
  for all to authenticated using (true) with check (true);
create policy "authenticated_all" on transactions
  for all to authenticated using (true) with check (true);

